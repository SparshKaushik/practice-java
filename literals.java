// Write a Java program demonstrating the usage of literal data types.

public class literals {
    public static void main(String[] args) {
        int a = 10; // decimal
        float b = 10.5f; // float
        double c = 10.5; // double
        int d = 0b1010; // binary
        int e = 010; // octal
        int f = 0x10; // hexadecimal
        char g = 'A'; // character
        String h = "Hello"; // string
        String i = null; // null
        boolean j = true; // boolean
        char k = '\u0061'; // unicode
        char l = 97; // unicode
        System.out.println("Integer: " + a);
        System.out.println("Float: " + b);
        System.out.println("Double: " + c);
        System.out.println("Binary: " + d);
        System.out.println("Octal: " + e);
        System.out.println("Hexadecimal: " + f);
        System.out.println("Character: " + g);
        System.out.println("String: " + h);
        System.out.println("Null: " + i);
        System.out.println("Boolean: " + j);
        System.out.println("Unicode: " + k);
        System.out.println("Unicode: " + l);
    }
}